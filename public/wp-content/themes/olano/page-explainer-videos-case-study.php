                <h3>The situation</h3>
                <p>As part of a larger project around the ITIL standard, the client needed to explain the difference
between incidents, problems and changes to a varied audience. The aim was to clearly differentiate
each situation in order for them to be managed and actioned effectively. This could be achieved
with a short video.</p>
                <h3>Prompts for Action</h3>
                <p>The solution should be easy to understand and generic as a metaphor to help explain the differences
regardless of the function or department.</p>
                <p>To further help things to seem more generic we would use character animation to simplify the steps
in the process as an alternative to using realistic video footage. To effectively complete the task an
explainer video would be created. Starting with a script, then a storyboard with mock-up still frames
before finally moving into the animation and synchronisation of audio.</p>
                <h3>Consequences</h3>
                <p>The risk was that we didn’t stray too far with the metaphoric example that it becomes to detached
from reality. The purpose is to simplify but yet captivate the audience in a way they can relate.</p>
                <h3>Outcomes</h3>
                <p>A three minute animation was developed, using our custom made characters which complimented
the client’s branding and contained professional narration.</p>
                <p>The metaphor was two conveyor lines carrying a different coloured box on each line, distributed to
separate locations. A sorting machine separates the boxes before they go on the conveyors. At
which point a single issue occurs with the machine leading to one box appearing on the wrong
conveyor. A workaround fix is applied. But then, the issue happens again. This time problem solving
is now needed to get to the route cause. A solution is applied and the problem is fixed. Finally, to
explain a change process a client asks for a change to made to the machine which would make it
able to recolour boxes, the change management process takes place and the machine is upgraded to
do the requested function. All in all, a simple but bespoke animation that nicely showcases the
differences within one story.</p>
                <h3>Moral</h3>
                <p>The animation was only 3 minutes but the complexity of the scenes, moving objects and parallax
effects between the foreground and background was intense to plan and develop. However, these
niceties all added up to a solution which got a fantastic response from the client.</p>
                <p>Giving a little extra here and there, makes for so much more than what was originally imagined.</p>
