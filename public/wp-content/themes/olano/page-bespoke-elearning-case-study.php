                <h3>The situation</h3>
                <p>Brightest Bulb are a fantastic company that train people how to create employee generated, professional looking videos on a smartphone.</p>
                <p>Their courses are great, but they needed our help transitioning from face to face workshops to
                digital online training.</p>
                <h3>Prompts for Action</h3>
                <p>This transition is something we specialise in, so we shared a few options of how we could achieve
                their desired outcome and suggested a route that best catered for long video tutorials and large file
                sizes.</p>
                <p>Key additions included regular interactivity to keep the users engaged and plenty of animation to
                complement the face to camera shots.</p>
                <h3>Consequences</h3>
                <p>We collectively wanted to maintain the experience and depth of the skills delivered during their
                existing workshops. With a lot of content to cover it would be important to ensure the programme
                was bitesize.</p>
                <p>Each module would then be progressively assessed, meaning that progress through to the end of the
                course could only be achieved once each stage was passed. Because of the depth of the theory it was also imperative that the leaner would be given simple tasks and go away and do before
                returning to the course. These tasks would also need to be inviting and easy to accomplish once
                given direction.</p>
                <p>Such large amounts of video meant for careful consideration towards the hosting of the video held
                on a dedicated streaming service. We must ensure the user’s learning experience was not
                interrupted by loading times or poor quality footage.</p>
                <h3>Outcomes</h3>
                <p>We ended up producing an elearning programme consisting of 10 modules that captured all the key
                elements of the original iDEC training. This programme was designed to be done over a longer
                period to give learners opportunity to complete their filming tasks on their smartphones. It
                empowers users with the knowledge of how to shoot, edit and distribute video on their smartphone
                using some basic applications and industry knowledge.</p>
                <p>This modality of the programme really benefits those that cannot attend workshops, or those who
                simply prefer to learn from the comfort of their own home.</p>
                <p>Due to the size of the course the variety of interaction and knowledge checking styles was vast.</p>
                <h3>Moral</h3>
                <p>One of the main challenges we faced was with file sizes, as the course included a large quantity of
                high-res videos, which along with our own animations, had the potential to increase loading times -
                which is not something we wanted the users to experience.</p>
                <p>To tackle this the files were held on a dedicated server and with some smart video wrapping they
                were embedded into the training which kept the actual modules small in file size. There was also the
                ability to stream a slighter lower quality version of the footage if the user is located within an area of
                limited internet connectivity, but for those in a better area they would still see the HD version.</p>
                <p>With the aid of our internal software team we now have a great solution of managing masses of
                video for similar courses in the future – this is valuable and aligns with the world today, a world
                which strives for 4K or higher videos streaming in an instant.</p>
