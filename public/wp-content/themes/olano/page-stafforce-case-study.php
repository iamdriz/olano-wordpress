                <h3>The situation</h3>
                <p>As part of a tender bid presentation, the client was wishing to take a less linear
approach towards a sales presentation that they had.</p>
                <p>Moving away from “death by PowerPoint” to a more interactive and engaging
presentation that included animation and video. A way that could be opened up to
the audience, providing flexibility in the way it is presented.</p>
                <h3>Prompts for Action</h3>
                <p>We regularly encourage the use of animation and video, therefore inspiring us
immediately to create a more interactive solution for them.</p>
                <p>Due to the competitive nature of sales pitching our recommendation was to keep the
PowerPoint format in order to keep it classroom and facilitator friendly whilst
heightening the engagement and interactivity using deep dive click throughs,
animations and videos.</p>
                <p>It was also important to keep it in line with the company’s branding to ensure
consistency and memorability after pitching for potential clients.</p>
                <h3>Consequences</h3>
                <p>We decided to keep text minimal within the presentation in order to step away from it
being a generic slideshow and instead become more of an interaction. The knock-on
effect of this was that in turn we had to find ways to display information that would
have normally been quite complex to explain.</p>
                <p>In particular, there was a large section centring around the onboarding process of
the employee which often took the facilitator a long time to explain due to the
technicalities of this. They were finding that due to the long windedness of the
explanation audiences were becoming disengaged within minutes. The solution was
to create a short video that encompassed all this information in a fun way, including
characters, animation and explanations to make it easier to understand. This also
provided the facilitator time to compose themselves for the rest of the presentation.</p>
                <p>With this being a dedicated sales presentation, it meant that there was a need for the
client to stand out amongst their competitors. We, therefore, had to ensure the
animations in the PowerPoint created as much interaction as possible for both the
facilitator and audience to promote the openness element that was required whilst
keeping to the company’s branding and culture. Interaction was going to be the way
to succession!</p>
                <h3>Outcomes</h3>
                <p>We ended up creating an interactive presentation consisting of different animations,
deep dive click throughs and explainer videos capturing all the information the client
wished to put out. This presentation was designed to have high impact and
memorability on the client’s audiences whilst being concise and catchy, as such to
ensure that no one was drifting away with their interest or becoming disengaged.</p>
                <p>This modality of blending the use of video, animation and other digital tools into a
classroom or in person setting is beneficial in providing large amounts of information
in a short period of time and allows flexibility for editing by the facilitators.</p>
                <p>We have also found that video is a better way for information to be retained which
was one of the key aims of the sales presentations created.</p>
                <p>And most importantly it took away any risk of “death by PowerPoint”!</p>
                <h3>Moral</h3>
                <p>One of the biggest challenges for us was to try and find a way to cater to every angle
and flow of delivery as normally this can be extremely tricky. With there being the
potential of multiple facilitators using this presentation as well as varied audiences
we had to find a solution where it was flexible in its usage whilst keeping the
interactive tone and client branded.</p>
                <p>We decided that the best way forward was to keep the traditional format of a
presentation so that it could still be easily edited by the facilitator and by
incorporating the interactivity in a more flexible manner so that it didn’t always have
to run in a specific order. This provided choice for both parties!</p>
                <p>Also, with so many Subject Matter Experts involved this could create some
communication barriers, so we always work towards having one point of sign off
whilst listening to all the best and relevant people within the company.</p>
